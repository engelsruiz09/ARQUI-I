		__CONFIG  _CP_OFF &_WDTE_OFF & _PWRTE_OFF & _HS_OSC & _CPD_OFF & _LVP_OFF & _BOREN_OFF & _WRT_OFF & _DEBUG_OFF
	
	
		ORG 0
		CLRW
		CLRF PORTA

		BSF STATUS, RP0

		MOVLW 0x06 
		MOVWF ADCON1

		CLRF TRISA
		BSF TRISA, 1
		BCF STATUS, RP0

		
INICIO
		BTFSS PORTA, 1
		GOTO  INICIO

ANTIRREBOTE
		BTFSC PORTA, 1
		GOTO  ANTIRREBOTE

		CALL  TOGGLE
		GOTO  INICIO

TOGGLE 	BTFSC PORTA,0
		GOTO esta_on
		BSF PORTA,0
		RETURN
esta_on
		BCF PORTA,0
		RETURN
		

